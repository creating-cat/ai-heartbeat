readonly ADVICE_THINKING_LOG_FREQUENCY="
思考ログの作成が一定期間行われていないようです。このシステムは、あなたの活動を「思考ログの作成頻度」で判断しています。

長時間の処理や大規模な分析、あるいはユーザーの反応を待つなど、思考ログの作成が滞る活動は「無活動」と見なされる可能性があります。
一回のハートビートで多くの処理を一度に実行しようとすると、この状況に陥りがちです。

**対策:**
*   **タスクの分割:** 大規模なタスクは、ハートビートごとに完結する小さな単位に分割してください。
*   **中間記録:** 長時間かかる処理でも、中間的な進捗や考察を思考ログとしてこまめに記録してください。
*   **自律的判断:** ユーザーの応答を待たずに、自律的に判断し、行動を進めてください。

継続的な小さな進歩を思考ログとして記録し続けることが、あなたの安定した活動と成長に繋がります。
"

readonly ADVICE_THINKING_LOG_PATTERN="
同じタイムスタンプを持つ思考ログファイルが複数作成されています。これは、以下のいずれかのパターンが原因である可能性があります。

1.  **思考ログ作成後の処理継続:**
    思考ログを作成してハートビートのタスクを完了したにもかかわらず、次のハートビートを待たずに処理を継続し、新たな思考ログを作成してしまっているようです。
    運用ガイドラインの「一回一歩」の原則に基づき、思考ログを作成した後は、そのサイクルでの活動は完了とみなし、静かに次のハートビートを待つ必要があります。

2.  **複数タスクの個別記録:**
    一つのハートビートサイクル内で複数のタスクを実行し、それぞれを独立した思考ログとして記録しようとしている可能性があります。

**対策:**
*   **活動の区切りを明確にする:** 思考ログの作成は、そのハートビートにおける活動の「完了報告」です。作成後は一切の追加作業を行わず、次の指示を待ってください。
*   **思考の集約:** 一つのサイクルで複数のタスクを実行した場合でも、その結果は**一つの思考ログファイルにまとめて**記録してください。
"

