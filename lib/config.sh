#!/bin/bash

# 設定管理ライブラリ
# heartbeat.shから分離された設定読み込みと定数定義

# 設定ファイル読み込み関数
load_config() {
    local config_file="$1"
    if [ -f "$config_file" ]; then
        source "$config_file"
        return 0
    else
        echo -e "\033[1;31m[ERROR]\033[0m 設定ファイルが見つかりません: $config_file"
        return 1
    fi
}

